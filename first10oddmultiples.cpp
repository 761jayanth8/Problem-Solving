vector<int> check10(int x){
    vector<int> out;
    int size = 0;
    // 1 not counted as odd multiple
    for(int i=3; i < (x/2) && size != 10; i+=2){
        if(x%i == 0){
            out.push_back(i);
            size++;
        }
    }
    return out;
}

